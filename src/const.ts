/**
 * Conversion factor from radians to degrees.
 *
 * @constant {number} RAD2DEG
 */
export const RAD2DEG: number = 180 / Math.PI;

/**
 * Conversion factor from degrees to radians.
 *
 * @constant {number} DEG2RAD
 */
export const DEG2RAD: number = Math.PI / 180;

/**
 * Half of Pi (π/2).
 *
 * @constant {number} HALF_PI
 */
export const HALF_PI: number = Math.PI / 2;

/**
 * Quarter of Pi (π/4).
 *
 * @constant {number} QUARTER_PI
 */
export const QUARTER_PI: number = Math.PI / 4;

/**
 * Two times Pi (2π).
 *
 * @constant {number} TWO_PI
 */
export const TWO_PI: number = Math.PI * 2;

/**
 * Tau, which is equivalent to 2π.
 *
 * @constant {number} TAU
 */
export const TAU: number = TWO_PI;
